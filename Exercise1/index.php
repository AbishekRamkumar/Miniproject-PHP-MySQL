<h2>Welcome to our college</h2>
<p>
The K. R. College of Arts & Science was established by the benevolent philanthropist Thiru K. Ramasamy, the Chairman of National Educational and Charitable Trust, Kovilpatti.This trust was established in 1984-1985 under the Chairmanship of Thiru K. Ramasamy.The first educational institution originated from the trust was National Engineering College. The Chairman decided to start a self-financing Arts & Science College in the style and name of K. R. College of Arts & Science (Co-Education). The College is being constructed in the land of 10 acres and 5.5 cents in Kovilpatti on the National High ways NH7 at a distance of about 4KM from Kovilpatti town, amidst a good surroundings in the rural area. The aim of this institution is to impart higher education to economically poor students in the vicinity of Kovilpatti
<br/> 
The Government of TamilNadu has granted permission to start this Co-Educational institution in the year 1997-1998 vide its Order No. MS. 315 dated 13.06.1997. The Manonmaniam Sundaranar University, Tirunelveli has granted affiliation to this college vide its Letter No.MSU/ CD/ Fresh-Aff/ 97-98 dated 22-08-1997. The College started functioning from 29-08-1997. Now, our college has completed 2 decades of academic service to rural students.
